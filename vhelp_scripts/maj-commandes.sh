# Pour mettre à jour les paquets de votre système, utilisez la commande suivante :

sudo apt-get update

# Pour installer les mises à jour disponibles, utilisez la commande suivante :

sudo apt-get upgrade

# Pour installer de nouveaux paquets qui n'ont pas été installés auparavant, utilisez la commande suivante :

sudo apt-get install <nom_du_paquet>

# Si vous utilisez une distribution Linux basée sur Debian ou Ubuntu, vous pouvez également utiliser la commande suivante pour mettre à jour l'ensemble du système :

sudo apt-get dist-upgrade

# Pour mettre à jour le noyau de votre système (systèmes basés sur Debian ou Ubuntu), utilisez la commande suivante :

sudo apt-get install linux-image-generic
